function resolveLocale() {
  const saved = localStorage.getItem('locale')
  if (saved === 'en' || saved === 'sv') return saved

  const browser = (navigator.language || 'en').toLowerCase()
  if (browser.startsWith('sv')) return 'sv'
  return 'en'
}

